        <h1 style="text-align:center; font-weight:bold;">Six Pillars of Smart City</h1>
        <p>A Smart City framework typically comprises six interconnected pillars: Smart Economy, Smart People, Smart
            Governance, Smart Mobility, Smart Environment, and Smart Living. Each pillar represents a domain of urban
            development where digital technologies and innovative practices are applied to improve efficiency,
            sustainability and quality of life. These pillars were originally defined in the European Smart Cities model
            (Giffinger et al., 2007) and have been adopted in various international frameworks (including Indonesia’s
            Smart
            City roadmap). Below we define each pillar, outline its key components, and give examples of related
            initiatives.</p>

        <h3>Smart Economy</h3>
        <img src="{{ asset('images/eco.png') }}"
            alt="Smart Economy" style="width:100%;max-width:400px;border-radius:12px;margin-bottom:1rem;">
        <p>This pillar focuses on economic vitality through innovation and competitiveness. A Smart Economy uses
            information
            and communication technologies (ICT) to strengthen local businesses, attract investment and talent, and
            develop
            sustainable industries.<br>
            <em>Sources: beesmart.city, researchgate.net</em>
        </p>
        <ul>
            <li><strong>Innovation and entrepreneurship:</strong> technology clusters, incubators and start-up
                ecosystems
                that foster creative industries and new business models. For example, cities often establish technology
                parks or offer grants to encourage high-tech firms.</li>
            <li><strong>Business-friendly environment:</strong> streamlined regulations and digital government services
                reduce barriers for companies (e.g. online permitting and e-payments).</li>
            <li><strong>Flexible labor market:</strong> training and education programs (including lifelong learning)
                build
                a skilled workforce adaptable to new industries.</li>
            <li><strong>Global integration:</strong> participation in international markets and networks (through
                digital
                platforms, export facilitation, and foreign investment) enhance local competitiveness.</li>
            <li><strong>Resource efficiency and sustainability:</strong> promoting green and circular economy practices
                so
                that economic growth also benefits social welfare.</li>
        </ul>
        <p><strong>Example:</strong> Singapore’s “Smart Nation” initiative leverages digital platforms and start-up
            funding
            to boost its economy. In Indonesia, smart city programs encourage micro-, small and medium enterprises
            (MSMEs)
            to adopt e‑commerce and digital tools, strengthening local entrepreneurship and creative industries.<br>
            <em>Source: ycp.com</em>
        </p>

        <h3>Smart People</h3>
        <img src="{{ asset('images/peo.png') }}"
            alt="Smart People" style="width:100%;max-width:400px;border-radius:12px;margin-bottom:1rem;">
        <p>The Smart People pillar refers to the social and human capital of the city – namely, the education, skills,
            creativity and civic engagement of its residents. A smart city invests in its citizens’ abilities and
            inclusiveness.<br>
            <em>Sources: researchgate.net, beesmart.city</em>
        </p>
        <ul>
            <li><strong>Education and lifelong learning:</strong> high-quality schools, vocational training and online
                courses that improve qualifications and digital literacy.</li>
            <li><strong>Creativity and culture:</strong> a vibrant cultural scene and tolerance for diversity (social
                and
                ethnic plurality) that attract and retain talented, open-minded people.</li>
            <li><strong>Civic participation:</strong> platforms for public involvement (such as participatory budgeting
                or
                crowdsourcing apps) encourage people to contribute ideas and feedback.</li>
            <li><strong>Social inclusion:</strong> reducing inequality by ensuring all community groups have access to
                opportunities and services (e.g. programs for the elderly, minorities, etc.).</li>
        </ul>
        <p>For instance, many smart cities run hackathons or community coding workshops to boost innovation.
            Beesmart.city
            emphasizes that Smart People “support the creation of an accessible and inclusive environment” and promote
            open-mindedness, participation and creativity. In Indonesia, programs to increase digital literacy (like
            online
            learning during the pandemic) and mobile reporting apps (allowing citizens to submit urban problem reports)
            illustrate efforts to empower smart, tech-savvy communities.<br>
            <em>Source: ycp.com</em>
        </p>

        <h3>Smart Governance</h3>
        <img src="{{ asset('images/gov.png') }}"
            alt="Smart Governance" style="width:100%;max-width:400px;border-radius:12px;margin-bottom:1rem;">
        <p>This pillar covers the use of ICT by public institutions to enhance transparency, efficiency and citizen
            engagement. Smart Governance means that government and its stakeholders collaborate through digital
            platforms
            and data-driven processes.<br>
            <em>Sources: beesmart.city, researchgate.net</em>
        </p>
        <ul>
            <li><strong>E-government services:</strong> offering online portals for permits, taxes, healthcare
                appointments,
                etc., so citizens can interact with government 24/7.</li>
            <li><strong>Open data and transparency:</strong> publishing city data (budgets, traffic, environmental
                metrics)
                to build trust and allow third parties to build solutions.</li>
            <li><strong>Participatory decision-making:</strong> tools like e-voting, public consultations or
                crowdsourced
                policy-making that let citizens directly influence decisions.</li>
            <li><strong>Inter-agency collaboration:</strong> integrated city dashboards or “command centers” that
                aggregate
                real-time data across departments (e.g. traffic, utilities, emergencies) to improve responsiveness.</li>
            <li><strong>Accountability and strategy:</strong> clear policies and long-term ICT strategies ensure
                consistent
                smart-city development (e.g. digital identification systems, privacy frameworks).</li>
        </ul>
        <p>For example, the beesmart.city model notes that Smart Governance “strengthen[s] connections” between
            government
            and society by using co-creation and crowdsourcing. Many cities worldwide have digital public service
            portals
            and open-data initiatives. In Indonesia, Jakarta’s Smart City office uses an integrated command center and
            mobile apps to manage services and encourage citizen feedback. Even e-residency and e-government systems in
            countries like Estonia exemplify this pillar by allowing citizens extensive online access to government
            services.<br>
            <em>Source: ycp.com</em>
        </p>

        <h3>Smart Mobility</h3>
        <img src="{{ asset('images/mob.png') }}"
            alt="Smart Mobility" style="width:100%;max-width:400px;border-radius:12px;margin-bottom:1rem;">
        <p>This pillar addresses the design of efficient, inclusive and sustainable transportation systems. Smart
            Mobility
            uses technology to optimize how people and goods move in the city.<br>
            <em>Source: beesmart.city</em>
        </p>
        <ul>
            <li><strong>Integrated public transit:</strong> seamless connections between buses, trains, subways and
                ferries,
                often enabled by unified ticketing or mobile trip-planning apps.</li>
            <li><strong>Real-time information:</strong> tracking of vehicle locations and traffic flows so commuters can
                make data-driven choices (e.g. apps that show bus arrivals or congestion heat maps).</li>
            <li><strong>Intelligent transport systems (ITS):</strong> adaptive traffic signals and smart parking that
                respond to real-time conditions to reduce jams.</li>
            <li><strong>Eco-friendly vehicles and infrastructure:</strong> support for electric or hydrogen buses,
                bike-sharing programs, charging stations and pedestrian-friendly streets.</li>
            <li><strong>Mobility-as-a-Service:</strong> digital platforms that combine multiple transport modes
                (ride-hailing, car-sharing, bike rentals) into a single on-demand service.</li>
        </ul>
        <p>The beesmart.city description explains Smart Mobility as improving efficiency and service quality of urban
            transport and adopting new, environmentally friendly modes. For example, Singapore’s “Walk Cycle Ride”
            initiative integrates walking and cycling paths with its metro and bus networks to encourage active mobility
            and
            reduce pollution. Indonesia’s smart mobility efforts include advanced traffic control systems (Adaptive
            Traffic
            Control System) and partnerships with ride-hailing services to improve flow and reduce idle time.<br>
            <em>Source: ycp.com</em>
        </p>

        <h3>Smart Environment</h3>
        <img src="{{ asset('images/env.png') }}"
            alt="Smart Environment" style="width:100%;max-width:400px;border-radius:12px;margin-bottom:1rem;">
        <p>This pillar deals with environmental sustainability and resource management. A Smart Environment uses
            sensors,
            data and green technologies to improve urban ecosystems.<br>
            <em>Source: beesmart.city</em>
        </p>
        <ul>
            <li><strong>Environmental monitoring:</strong> deploying IoT sensors to track air and water quality, noise
                levels, and waste production for timely responses.</li>
            <li><strong>Energy management:</strong> smart grids and metering, plus investment in renewables (solar
                panels,
                wind turbines) to reduce carbon footprint.</li>
            <li><strong>Green infrastructure:</strong> creating and maintaining parks, green roofs and permeable
                surfaces to
                improve biodiversity and stormwater control.</li>
            <li><strong>Waste and water systems:</strong> smart bins for optimized collection, and digital
                leak-detection in
                water networks to conserve resources.</li>
            <li><strong>Regulatory tools:</strong> enforcing building standards, pollution controls and sustainable
                urban
                planning.</li>
        </ul>
        <p>According to beesmart.city, Smart Environment focuses on managing the built and natural environment with
            technology to implement sustainable standards. An example is Barcelona’s “superblocks” project, which
            redesigns
            neighborhoods to cut through-traffic – this has led to better air quality and more green public space. Many
            cities also set carbon-neutrality goals (e.g. Copenhagen aims to be carbon-neutral by 2025). In Indonesia,
            some
            local governments use online platforms and sensor networks to track pollution and flooding, aligning with
            smart-environment objectives.<br>
            <em>Source: ycp.com</em>
        </p>

        <h3>Smart Living</h3>
        <img src="{{ asset('images/liv.png') }}"
            alt="Smart Living" style="width:100%;max-width:400px;border-radius:12px;margin-bottom:1rem;">
        <p>The final pillar, Smart Living, covers the overall quality of life and well-being of residents. It includes
            healthcare, education, housing, safety and culture.<br>
            <em>Source: beesmart.city</em>
        </p>
        <ul>
            <li><strong>Healthcare services:</strong> telemedicine platforms, health-monitoring devices, and connected
                hospitals that improve access to care.</li>
            <li><strong>Education and skills:</strong> e-learning systems and digital classrooms that expand learning
                opportunities across age groups.</li>
            <li><strong>Housing and urban design:</strong> smart homes and energy-efficient buildings, as well as
                affordable
                housing policies that enhance livability.</li>
            <li><strong>Public safety and emergency response:</strong> smart surveillance (with safeguards for privacy),
                emergency alert apps, and data analytics to improve policing and disaster readiness.</li>
            <li><strong>Community and culture:</strong> promoting social cohesion through inclusive public spaces,
                cultural
                facilities and access to information (e.g. municipal Wi-Fi and smart libraries).</li>
        </ul>
        <p>Beesmart.city notes that Smart Living targets “inclusive” improvements in livability, healthcare (like
            e-health
            and assisted living), education, housing and safety. For example, Singapore has developed HealthCity Novena
            – an
            integrated healthcare campus with connected walkways and parks – to enhance patient wellbeing. Its public
            housing system (HDB) provides high-quality, mixed-use neighborhoods for most citizens. In Indonesia,
            smart-living initiatives include online public health consultations and remote learning platforms used
            during
            COVID-19, reflecting a move toward more connected, responsive social services.<br>
            <em>Source: ycp.com</em>
        </p>

        <p>Each of these six pillars – Economy, People, Governance, Mobility, Environment, and Living – must advance
            together to achieve a truly smart city. In practice, city planners align policies and technologies across
            these
            domains (sometimes with local variations) to create sustainable, inclusive and efficient urban
            ecosystems.<br>
            <em>Sources: beesmart.city, researchgate.net</em>
        </p>
